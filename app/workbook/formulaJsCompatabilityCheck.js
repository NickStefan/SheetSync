function formulaJsCompatibilityCheck (formula){
  var formularRegex = /\b[A-Za-z2]+(?=\()(?![^']*'!)/g;
  var formularArr = formula.match(formularRegex);
  if(formularArr !== null){
    for(var i = 0;i < formularArr.length;i++){
      if(!(formularArr[i] in supportedFormulas)){
        return false;
      }
    }
  }
  return true;
}
